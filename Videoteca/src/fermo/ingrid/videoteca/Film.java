package fermo.ingrid.videoteca;

public class Film {
	private int giorni;
	private String titolo;
	private String nome_regista;
	private int anno;
	private String nome_attori;
	
	public int preventivo(){
		
	}

	public int getGiorni() {
		return giorni;
	}

	public void setGiorni(int giorni) {
		this.giorni = giorni;
	}

	public String getTitolo() {
		return titolo;
	}

	public void setTitolo(String titolo) {
		this.titolo = titolo;
	}

	public String getNome_regista() {
		return nome_regista;
	}

	public void setNome_regista(String nome_regista) {
		this.nome_regista = nome_regista;
	}

	public int getAnno() {
		return anno;
	}

	public void setAnno(int anno) {
		this.anno = anno;
	}

	public String getNome_attori() {
		return nome_attori;
	}

	public void setNome_attori(String nome_attori) {
		this.nome_attori = nome_attori;
	}
	
	
}
